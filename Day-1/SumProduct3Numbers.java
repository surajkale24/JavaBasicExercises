package com.basic_example;

public class SumProduct3Numbers {

	public static void main(String[] args) {

		int no1=98;
		int no2=5;
		int no3=77;
		int sum, product;
		
		sum=no1+no2+no3;
		product=no1*no2*no3;
		
		System.out.println("THE SUM AND PRODUCT OF "+no1+","+no2+" AND "+no3+" ARE "+sum+" AND "+product);
	}

}
