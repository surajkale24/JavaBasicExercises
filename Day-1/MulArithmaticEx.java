package com.basic_example;

public class MulArithmaticEx {

	public static void main(String[] args) {

		int a=3,b=2;
		int no1,no2,sum;
		
		no1=a;
		no2=b;
		
		int x=31*a+17*b;
		sum=x;
		
		System.out.println("NO1 IS:"+no1 );
		System.out.println("NO2 IS:"+no2 );
		System.out.println("THE SUM IS:"+sum);
	}

}
